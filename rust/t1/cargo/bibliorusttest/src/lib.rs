#![allow(non_snake_case)]

pub fn add(left: u64, right: u64) -> u64 {
    left + right
}

/// Calcule le carré d'un entier.
///
/// # Arguments
///
/// * `x` - L'entier dont on calcule le carré.
///
/// # Retour
///
/// Retourne le carré de `x`.
pub fn CommentExample(ch: char, int: u8) -> u16 {
    println!("{}{}", ch, int);
    return 56;
}

